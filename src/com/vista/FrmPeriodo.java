    public void insertar(){
        try {
           peri.setPeriodo(this.jTxtPeriodo.getText().trim());
           peri.setDescripcion(this.jCbDescripcion.getSelectedItem().toString());
           daop.insertarPeriodo(peri);
           JOptionPane.showMessageDialog(null,"Datos Insertado Correctamente");
           tablaE();
           limpiar();
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this,e.toString(),"ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void llenarTabla(){
        int fila=this.jTablaPeriodo.getSelectedRow();
        this.jTxtCodigoPeriodo.setText(String.valueOf(this.jTablaPeriodo.getValueAt(fila, 0)));
        this.jTxtPeriodo.setText(String.valueOf(this.jTablaPeriodo.getValueAt(fila, 1)));
        this.jCbDescripcion.setSelectedItem(String.valueOf(this.jTablaPeriodo.getValueAt(fila, 2)));
    }
    
    public void limpiar(){
        this.jTxtPeriodo.setText("");
        this.jCbDescripcion.setSelectedIndex(0);
    }
    
    public void modificar(){
        try {
            peri.setCodperiodo(Integer.parseInt(this.jTxtCodigoPeriodo.getText().trim()));
            peri.setPeriodo(this.jTxtPeriodo.getText().trim());
            peri.setDescripcion(this.jCbDescripcion.getSelectedItem().toString());
            int SiONo=JOptionPane.showConfirmDialog(this, "Desea modificar el Periodo",
                    "Modificar Periodo",JOptionPane.YES_NO_OPTION);
            if(SiONo==0){
                daop.modificarPeriodo(peri);
                JOptionPane.showMessageDialog(rootPane, "Periodo modificado con exito", 
                        "Confirmación",
                        JOptionPane.INFORMATION_MESSAGE);
                tablaE();
                limpiar();
            }
            else{
                limpiar();
            }
        }catch (Exception ex) {
           ex.printStackTrace();
        }
    }
    public void eliminar(){
        try {
            peri.setCodperiodo(Integer.parseInt(this.jTxtCodigoPeriodo.getText()));
            int SiONo=JOptionPane.showConfirmDialog(this, "Desea eliminar el Departamento",
                    "Eliminar Departamento",JOptionPane.YES_NO_OPTION);
            if(SiONo==0){
                daop.eliminarPeriodo(peri);
                JOptionPane.showMessageDialog(rootPane,"Eliminado con exito" , "Confirmación",
                        JOptionPane.INFORMATION_MESSAGE);
                tablaE();
                limpiar();
            }else{
                limpiar();
            }
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e.toString(), "Error",
                    JOptionPane.ERROR_MESSAGE);
        } 
    }
    public void tablaE(){
        String [] columnas={"CodigoPeriodo","NombrePeriodo","Descripcion"} ;
        Object[] obj=new Object[3];
        DefaultTableModel tabla=new DefaultTableModel(null,columnas);
        List ls;
        try{
            ls =daop.mostrarPeriodo();
            for(int i=0;i<ls.size();i++){
                peri = (Periodo)ls.get(i);
                obj[0] = peri.getCodperiodo();
                obj[1] = peri.getPeriodo();
                obj[2] = peri.getDescripcion();
                tabla.addRow(obj);
            }
            ls = daop.mostrarPeriodo();
            this.jTablaPeriodo.setModel(tabla);
        } 
        catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error al mostrar datos" + e.toString());
        }
    }
    
