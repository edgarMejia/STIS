    private void jTxtBusquedaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtBusquedaKeyPressed
        String [] columnas={"CodigoPeriodo","Nombre Periodo","Descripcion"} ;
        Object[] obj=new Object[3];
        String sql="SELECT * FROM Periodo WHERE id_periodo LIKE '"
                +jTxtBusqueda.getText()+"%'"
                +"OR nombre_periodo LIKE '" + jTxtBusqueda.getText() + "%'"
                +"OR descripcion LIKE '" + jTxtBusqueda.getText() + "%';";
        DefaultTableModel model = new DefaultTableModel(null, columnas);
        Conexion con = new Conexion();
        try {
            con.conectar();
            ResultSet rs;
            Statement st = con.getCon().createStatement();
            rs=st.executeQuery(sql);
            while(rs.next()){
                obj[0] = rs.getString("id_periodo");
                obj[1] = rs.getString("nombreperido");
                obj[2] = rs.getString("descripcion");
                model.addRow(obj);
            }
            jTablaPeriodo.setModel(model);
        } catch (Exception e) {
            e.toString();
        }
    }//GEN-LAST:event_jTxtBusquedaKeyPressed


