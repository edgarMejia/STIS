    private void jTxtBusquedaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtBusquedaKeyReleased
        String [] columnas={"CodigoEncargado","NombreEncargado","DUI"} ;
        Object[] obj=new Object[3];
        String sql="SELECT * FROM Encargado WHERE id_encargado LIKE '%"
        +jTxtBusqueda.getText()+"%'"
        +"OR nombre_encargado LIKE '%" + jTxtBusqueda.getText() + "%'"
        +"OR dui LIKE '%" + jTxtBusqueda.getText() + "%';";
        DefaultTableModel model = new DefaultTableModel(null, columnas);
        Conexion con = new Conexion();
        try {
            con.conectar();
            ResultSet rs;
            Statement st = con.getCon().createStatement();
            rs=st.executeQuery(sql);
            while(rs.next()){
                obj[0] = rs.getString("id_encargado");
                obj[1] = rs.getString("nombre_encargado");
                obj[2] = rs.getString("dui");
                model.addRow(obj);
            }
            jTablaEncargado.setModel(model);
        } catch (Exception e) {
            e.toString();
        }
    }//GEN-LAST:event_jTxtBusquedaKeyReleased


