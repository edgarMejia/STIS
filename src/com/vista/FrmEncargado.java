    public void insertar(){
        try {
           en.setEncargado(this.jTxtNombreEncargado.getText().trim());
           en.setDui(this.jTxtDui.getText().trim());
           daoe.insertarEncargado(en);
           JOptionPane.showMessageDialog(null,"Datos Insertado Correctamente");
           tablaE();
           limpiar();
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this,e.toString(),"ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void llenarTabla(){
        int fila=this.jTablaEncargado.getSelectedRow();
        this.jTxtCodigoEncargado.setText(String.valueOf(this.jTablaEncargado.getValueAt(fila, 0)));
        this.jTxtNombreEncargado.setText(String.valueOf(this.jTablaEncargado.getValueAt(fila, 1)));
        this.jTxtDui.setText(String.valueOf(this.jTablaEncargado.getValueAt(fila, 2)));
    }
    
    public void limpiar(){
        this.jTxtNombreEncargado.setText("");
        this.jTxtDui.setText("");
    }
    
    public void modificar(){
        try {
            en.setCodencargado(Integer.parseInt(this.jTxtCodigoEncargado.getText().trim()));
            en.setEncargado(this.jTxtNombreEncargado.getText().trim());
            en.setDui(this.jTxtDui.getText().trim());
            int SiONo=JOptionPane.showConfirmDialog(this, "Desea modificar el Periodo",
                    "Modificar Periodo",JOptionPane.YES_NO_OPTION);
            if(SiONo==0){
                daoe.modificarEncargado(en);
                JOptionPane.showMessageDialog(rootPane, "Periodo modificado con exito", 
                        "Confirmación",
                        JOptionPane.INFORMATION_MESSAGE);
                tablaE();
                limpiar();
            }
            else{
                limpiar();
            }
        }catch (Exception ex) {
           ex.printStackTrace();
        }
    }
    public void eliminar(){
        try {
            en.setCodencargado(Integer.parseInt(this.jTxtCodigoEncargado.getText()));
            int SiONo=JOptionPane.showConfirmDialog(this, "Desea eliminar el Encargado",
                    "Eliminar Encargado",JOptionPane.YES_NO_OPTION);
            if(SiONo==0){
                daoe.eliminarEncargado(en);
                JOptionPane.showMessageDialog(rootPane,"Eliminado con exito" , "Confirmación",
                        JOptionPane.INFORMATION_MESSAGE);
                tablaE();
                limpiar();
            }else{
                limpiar();
            }
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e.toString(), "Error",
                    JOptionPane.ERROR_MESSAGE);
        } 
    }
    public void tablaE(){
        String [] columnas={"CodigoEncargado","NombreEncargado","DUI"} ;
        Object[] obj=new Object[3];
        DefaultTableModel tabla=new DefaultTableModel(null,columnas);
        List ls;
        try{
            ls =daoe.mostrarEncargado();
            for(int i=0;i<ls.size();i++){
                en = (Encargado)ls.get(i);
                obj[0] = en.getCodencargado();
                obj[1] = en.getEncargado();
                obj[2] = en.getDui();
                tabla.addRow(obj);
            }
            ls = daoe.mostrarEncargado();
            this.jTablaEncargado.setModel(tabla);
        } 
        catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error al mostrar datos" + e.toString());
        }
    }
    
