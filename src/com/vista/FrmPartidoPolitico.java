    public void guardarPartido() throws IOException, Exception{
        //EL PARTIDO SE GUARDARA VARIAS VECES PORQUE NO SE HA CREADO UN METODO
        //PARA MODIFICAR NI VALIDACIONES
        DaoPartidoPolitico dao = new DaoPartidoPolitico();
        PartidoPolitico partido = new PartidoPolitico();
        try{
            guardarFoto();
            partido.setPartido("Mauro guapo");
            partido.setPresidente("Mauro presidnete");
            partido.setFotoPartido(fotoPartido);
            partido.setActivo(1);
            dao.insertarPartido(partido);
            mostrarPartidos();
        }catch(Exception e){
            throw e;
        }
        
    }
    
    public void guardarFoto() throws IOException{
        JFileChooser file = new JFileChooser();
        file.setDialogTitle("Seleccione la imagen a guardar");
        
        int seleccion = file.showSaveDialog(file);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File foto = file.getSelectedFile();
            fotoPartido = extraerBytes(foto);
        }
    }
    
    public byte[] extraerBytes (File img) throws IOException {
        //Obtener imagen
        BufferedImage bufferedImage = ImageIO.read(img);

        // obtener DataBufferBytes del Raster
        WritableRaster raster = bufferedImage .getRaster();
        DataBufferByte data   = (DataBufferByte) raster.getDataBuffer();

        return ( data.getData() );
    }

